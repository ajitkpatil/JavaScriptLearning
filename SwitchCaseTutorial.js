//Switch....Case Tutorial
let season = 'winter';

switch(season){
    case 'summer':
        console.log('Time to wear light colored lose cloths');
        break;
    case 'monsoon':
        console.log('Time to carry an umbrell or a raincoat');
        break;
    case 'winter':
        console.log('Time to wear warm and cozy clothes');
        break;
    default:
        console.log('You are certainly not in India');
        break;
}