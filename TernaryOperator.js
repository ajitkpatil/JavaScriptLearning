//Ternary Operator Assignment
let isLocked = true;

if(isLocked){
    console.log('Unlock the door..');
}else{
    console.log('Lock the door');
}

/*Write the above if..else block using Ternary operator
Ternary operator uses ? (question mark) followed by two arguments 
and separated by a : (colon) sign
*/
isLocked ? console.log('Unlock the door') : console.log('Lock the door');