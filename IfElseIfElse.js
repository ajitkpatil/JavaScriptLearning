//if...else if...else..if..else block tutorial
let season = 'summer';

if (season === 'winter'){
    console.log('It\'s season to wear warm clothes');
}else if(season === 'monsoon'){
    console.log('It\'s season to carry an umbrella');
}else if(season === 'summer'){
    console.log('It\'s season to wear light color and lose clothes');
}else{
    console.log('No such season exists in India');
}